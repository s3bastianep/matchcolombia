export function bathroomFilterChipLabel(count) {
  if (count.includes(",")) {
    return `${count.split(",").join(" y ")} baños`;
  }
  if (count === "5") return "5+ baños";
  return `${count} baño${count !== "1" ? "s" : ""}`;
}
